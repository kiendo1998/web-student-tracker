package com.web.jdbc.foruser;

public class Diem {

	private int mamh;
	private String tenmh;
	private float dqt;
	private float diemthi;
	private String kyhoc;
	private float diemhe4;
	private String diemchu;
	
	
	
	
	public Diem(int mamh, String tenmh, float dqt, float diemthi, String kyhoc, float diemhe4, String diemchu) {
		this.mamh = mamh;
		this.tenmh = tenmh;
		this.dqt = dqt;
		this.diemthi = diemthi;
		this.kyhoc = kyhoc;
		this.diemhe4 = diemhe4;
		this.diemchu = diemchu;
	}
	public int getMamh() {
		return mamh;
	}
	public void setMamh(int mamh) {
		this.mamh = mamh;
	}
	public String getTenmh() {
		return tenmh;
	}
	public void setTenmh(String tenmh) {
		this.tenmh = tenmh;
	}
	public float getDqt() {
		return dqt;
	}
	public void setDqt(float dqt) {
		this.dqt = dqt;
	}
	public float getDiemthi() {
		return diemthi;
	}
	public void setDiemthi(float diemthi) {
		this.diemthi = diemthi;
	}
	public String getKyhoc() {
		return kyhoc;
	}
	public void setKyhoc(String kyhoc) {
		this.kyhoc = kyhoc;
	}
	public float getDiemhe4() {
		return diemhe4;
	}
	public void setDiemhe4(float diemhe4) {
		this.diemhe4 = diemhe4;
	}
	public String getDiemchu() {
		return diemchu;
	}
	public void setDiemchu(String diemchu) {
		this.diemchu = diemchu;
	}
	
	
	
	
	


	
	
	
	
	

}
